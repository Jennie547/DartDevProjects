/*  
 * This program prints an ASCII art representation of a pyramid   
 * using asterisks. Each line of the pyramid is printed separately   
 * to create a visual structure.  
 */  

void main() {  
  // Print the first line of the pyramid with one asterisk, centered  
  print("      *      ");   
  
  // Print the second line of the pyramid with three asterisks, centered  
  print("     ***     ");   
  
  // Print the third line of the pyramid with seven asterisks, centered  
  print("   *******   ");   
  
  // Print the fourth line of the pyramid with eleven asterisks, centered  
  print(" *********** ");   
  
  // Print the fifth line of the pyramid with fifteen asterisks, centered  
  print("*************");   
}