/*  
 * This program demonstrates variable declaration and   
 * printing their values in Dart. It defines variables   
 * of different types and outputs them to the console.  
 */  

void main() {  
  // Declare a String variable named 'performance' and assign it the value "intelligent"  
  String performance = "inteligent";   
  
  // Declare an integer variable named 'fortyFive' and assign it the value 45  
  int fortyFive = 45;   
  
  // Declare a double variable named 'decimal' and assign it the value 2.45  
  double decimal = 2.45;   
  
  // Declare a String variable named 'quality' and assign it the value "beautiful"  
  String quality = "beautiful";   
  
  // Print the value of the 'performance' variable to the console  
  print(performance);   
  
  // Print the value of the 'fortyFive' variable to the console  
  print(fortyFive);   
  
  // Print the value of the 'decimal' variable to the console  
  print(decimal);   
  
  // Print the value of the 'quality' variable to the console  
  print(quality);   
}